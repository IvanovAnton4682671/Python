# –ï—Å–ª–∏ —Ç–∞–∫–∏—Ö —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –Ω–µ—Å–∫–æ–ª—å–∫–æ, —Ç–æ –≤—ã–≤–æ–¥–∏—Ç—Å—è —Ç–æ—Ç, —É –∫–æ—Ç–æ—Ä–æ–≥–æ –º–µ–Ω—å—à–µ –Ω–æ–º–µ—Ä —Å—Ç—Ä–æ–∫–∏,
# –∞ –µ—Å–ª–∏ –Ω–æ–º–µ—Ä–∞ —Å—Ç—Ä–æ–∫ —Ä–∞–≤–Ω—ã —Ç–æ —Ç–æ—Ç, —É –∫–æ—Ç–æ—Ä–æ–≥–æ –º–µ–Ω—å—à–µ –Ω–æ–º–µ—Ä —Å—Ç–æ–ª–±—Ü–∞. –ü—Ä–æ–≥—Ä–∞–º–º–∞ –ø–æ–ª—É—á–∞–µ—Ç
# –Ω–∞ –≤—Ö–æ–¥ —Ä–∞–∑–º–µ—Ä—ã –º–∞—Å—Å–∏–≤–∞ ùëõ –∏ ùëö, –∑–∞—Ç–µ–º ùëõ —Å—Ç—Ä–æ–∫ –ø–æ ùëö —á–∏—Å–µ–ª –≤ –∫–∞–∂–¥–æ–π.

n = int(input("–í–≤–µ–¥–∏—Ç–µ –∫–æ–ª-–≤–æ —Å—Ç—Ä–æ–∫ –≤ –º–∞—Å—Å–∏–≤–µ: "))
m = int(input("–í–≤–µ–¥–∏—Ç–µ –∫–æ–ª-–≤–æ —Å—Ç–æ–ª–±—Ü–æ–≤ –≤ –º–∞—Å—Å–∏–≤–µ: "))
print("–í–≤–µ–¥–∏—Ç–µ —Å–∞–º –º–∞—Å—Å–∏–≤:")
matr = [[int(m) for m in input().split()] for row in range(n)]
for i in range(n):
    if len(matr[i]) < m:
        while len(matr[i]) < m:
            matr[i].append(0)
    if len(matr[i]) > m:
        matr[i] = matr[i][:m]
print("–í–∞—à –º–∞—Å—Å–∏–≤:")
for row in matr:
    for elem in row:
        print(elem, end=' ')
    print()

max_elem = 0
for row in matr:
    for elem in row:
        if elem > max_elem:
            max_elem = elem
print("–í—Å–µ –º–∞–∫—Å–∏–º–∞–ª—å–Ω—ã–µ —ç–ª–µ–º–µ–Ω—Ç—ã:")
for i in range(n):
    for j in range(int(len(matr[i]))):
        if matr[i][j] == max_elem:
            print("–ù–æ–º–µ—Ä —Å—Ç—Ä–æ–∫–∏ –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–≥–æ —ç–ª–µ–º–µ–Ω—Ç–∞: " + str(i) + ", –Ω–æ–º–µ—Ä —Å—Ç–æ–ª–±—Ü–∞ –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–≥–æ —ç–ª–µ–º–µ–Ω—Ç–∞: " + str(j))

count = 0
print("–ü–µ—Ä–≤–æ–µ –≤—Ö–æ–∂–¥–µ–Ω–∏–µ –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–≥–æ —ç–ª–µ–º–µ–Ω—Ç–∞:")
for i in range(n):
    for j in range(int(len(matr[i]))):
        if count == 0:
            if matr[i][j] == max_elem:
                print("–ù–æ–º–µ—Ä —Å—Ç—Ä–æ–∫–∏ –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–≥–æ —ç–ª–µ–º–µ–Ω—Ç–∞: " + str(i) + ", –Ω–æ–º–µ—Ä —Å—Ç–æ–ª–±—Ü–∞ –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–≥–æ —ç–ª–µ–º–µ–Ω—Ç–∞: " + str(j))
                count += 1
